    private void bttApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttApagarActionPerformed
        int resposta = JOptionPane.showConfirmDialog( null,"Confirma a exclusão de \n"+this.banco.get(cont).getNome(), "Exclusão de Registro",JOptionPane.YES_NO_OPTION);
        
        if(resposta == 0){
            this.banco.remove(cont);
            Atualiza();
        }  
        try {
            this.le.escrever_arquivo();
        } catch (IOException e) {
        }
    }//GEN-LAST:event_bttApagarActionPerformed

    private void jTextArea1txtAreaMouseClick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextArea1txtAreaMouseClick
        try {
            // descubro qual linha foi clicada
            int linha = this.jTextArea1.getLineOfOffset(jTextArea1.getCaretPosition());
            // defino o inicio do texto dessa linha
            int inicio = jTextArea1.getLineStartOffset(linha);
            // defino o final da linha
            int fim = jTextArea1.getLineEndOffset(linha);

            //gero um string a partir a linha que foi clicada
            String s = jTextArea1.getDocument().getText(inicio, fim-inicio-1);
            //this.jTextFieldBuscar.setText(s);

            this.jTextArea1.select(inicio, fim-1);

            for (int i = 0; i < banco.size(); i++) {

                if(banco.get(i).getNome().toLowerCase().contains(s.toLowerCase())){
                    cont = i;
                    this.Atualiza();

                }

            }

        } catch (BadLocationException ex) {
            Logger.getLogger(TelaRemover.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextArea1txtAreaMouseClick

