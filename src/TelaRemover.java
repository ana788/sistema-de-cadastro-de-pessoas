     public void Atualiza(){
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        if(banco.size()<=0){
            this.jTextNome.setText("Nada a exibir");
            this.jTextEndereco.setText("Nada a exibir");
            this.jTextCidade.setText("Nada a exibir");
            this.jTextDataNasc.setText("Nada a exibir");
            this.jTextAltura.setText("Nada a exibir");
            this.jTextNumero.setText("Nada a exibir");
            this.jLabelFoto.setText("Nada a exibir");
            this.jTextEstado.setText("Nada a exibir");
        } else{
        
            if(cont > banco.size()-1)
                cont = banco.size()-1;

            this.jTextNome.setText(this.banco.get(cont).getNome());
            this.jTextEndereco.setText(this.banco.get(cont).getEndereco());
            this.jTextCidade.setText(this.banco.get(cont).getCidade());
            this.jTextDataNasc.setText(String.valueOf(formato.format(this.banco.get(cont).getDataNasc())));
            this.jTextAltura.setText(String.valueOf(this.banco.get(cont).getAltura()));
            this.jTextNumero.setText(String.valueOf(this.banco.get(cont).getNumero()));
            this.jLabelFoto.setIcon(this.banco.get(cont).getFoto());
            this.jTextEstado.setText(this.banco.get(cont).getEstado().getNome());
        }
    }

